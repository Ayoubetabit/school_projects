#include "libft.h"

int	ft_isalnum(int x)
{
	if ((x >= 'a' && x <= 'z') || (x >= 'A' && x <= 'Z') || (x >= 1 && x <= 9))
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
