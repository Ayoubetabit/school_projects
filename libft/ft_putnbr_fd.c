#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	x;

	if (n == -2147483648)
		ft_putstr_fd("-2147483648", fd);
	if (n < 0)
	{
		write (fd, "-", 1);
		n = -n;
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
	}
	x = (n % 10) + '0';
	write (fd, &x, 1);
}
