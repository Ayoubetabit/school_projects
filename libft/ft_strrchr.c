char	*ft_strrchr(const char *s, int c)
{
	size_t	i;
	char	*n;

	i = 0;
	n = NULL;
	while (s[i])
	{
		if (s[i] == (char)c)
		{
			n = (char *)&s[i];
		}
		i++;
	}
	if (s[i] == (char)c)
	{
		return ((char *)&s[i]);
	}
	return (n);
}
